<?php
$y = "10";
$z = "9";

// Operator / → digunakan untuk pembagian biasa, hasilnya bisa berupa bilangan pecahan (float).
echo $y / $z; // 19
echo "<br />";

echo $y % $z; // 19
echo "<br />";

// Operator % (modulo) → digunakan untuk mencari sisa hasil bagi, hasilnya selalu bilangan bulat (integer).
?>